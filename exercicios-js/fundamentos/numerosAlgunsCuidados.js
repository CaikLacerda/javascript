// Números divididos por 0 gerará o valor Infinito
console.log(7 / 0)

// Você pode multiplicar strings que contenham valores number dentro dela
console.log("10" / 2)

// Algumas operações não terá valores exatos, por conta do padrão seguido pelo JavaScript
console.log(0.1 + 0.7)

// Multiplicar palavras gerará um NaN (Not a Number)
console.log("Show" * 2)

// Você pode chamar uma função para um número literal desde que ele esteja dentro de parênteses
console.log((10.233).toFixed(2))