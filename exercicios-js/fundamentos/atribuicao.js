const a = 7 // "=" eh uma atribuicao
let b = 3

b += a // b = b + a
console.log('Soma: ', b)

b -= 4 // b = b - 4
console.log('Subtracao: ',b)

b *= 2 // b = b * 2
console.log('Multiplicacao: ', b)

b /= 2 // b = b / 2
console.log('Divisao: ', b)

b %= 2 // b = b % 2 --- 0 = par, 1 = impar
console.log(b)

